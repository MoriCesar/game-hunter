sprites.onOverlap(SpriteKind.Projectile, SpriteKind.Bystander, function (sprite, otherSprite) {
    music.playTone(131, music.beat(BeatFraction.Sixteenth))
    if (sprite == freezeray) {
        sprite.destroy()
        otherSprite.startEffect(effects.coolRadial)
        CarHeath += -1
        if (otherSprite.x > scene.screenWidth() / 2) {
            otherSprite.setVelocity(60, sceneSpeed)
        } else {
            otherSprite.setVelocity(-60, sceneSpeed)
        }
    } else if (sprite == smoke) {
        sprite.destroy()
        otherSprite.startEffect(effects.warmRadial)
        CarHeath += -1
        if (otherSprite.x > scene.screenWidth() / 2) {
            otherSprite.setVelocity(60, sceneSpeed)
        } else {
            otherSprite.setVelocity(-60, sceneSpeed)
        }
    }
    info.changeScoreBy(-300)
})
sprites.onDestroyed(SpriteKind.Projectile, function (sprite) {
    if (sprite == freezeray) {
        freezeExists = false
    } else if (sprite == smoke) {
        smokeExists = false
    }
})
